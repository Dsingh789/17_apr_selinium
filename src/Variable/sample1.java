package Variable;

public class sample1 
{
	public static void main(String[] args) 
	{
		
		//variable declaration
		   String sname;               //dataType variableName;
		   int  srollNum;
		   float sper;
		   char sgrade;
		
		//variable initialization
		   sname="rahul";                  //variableName = "rahul";
		   srollNum=102;
		   sper=65.5f;
		   sgrade='A';
		   
		  //variable usage
		   System.out.println("Student name="+sname);
		   System.out.println("Student roll num="+srollNum);
		   System.out.println("Student percentage="+sper+"%");
		   System.out.println("Student grade="+sgrade);
		   System.out.println("Student grade="+sgrade);   //reuse	   
	}
}
